package root;

public class RatioRectangle {
    final double ratioSpecifiedX;
    final double ratioSpecifiedY;
    final double ratioWidth;
    final double ratioHeight;

    public RatioRectangle(double ratioSpecifiedX, double ratioSpecifiedY, double ratioWidth, double ratioHeight) {
        this.ratioSpecifiedX = ratioSpecifiedX;
        this.ratioSpecifiedY = ratioSpecifiedY;
        this.ratioWidth = ratioWidth;
        this.ratioHeight = ratioHeight;
    }
}
